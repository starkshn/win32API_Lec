#pragma once

class CObject;

class RigidBody
{
private:
	CObject* p_owner;

	Vector2 _force;			// 크기, 방향
	Vector2 _accel;			// 가속도 
	Vector2 _velocity;		// 속도 (크기 : 속력, 방향)

	float	_mass;			// 질량
	float	_frictCoeff;	// 마찰 계수
	float	_maxVelocity;	// 최대 속력

	// F = M * A;
	// 가속도가 누적되서 '속도'가 나옴.
	// V += A * DT; => 현재속도
	
public:
	RigidBody();
	~RigidBody();

public:
	void update();
	void finalUpdate();

private:
	void Move();

public:
	void AddForce(Vector2 force)
	{
		_force += force;
	}
	
	// 움직일 때 즉시 속력을 위한 함수
	void AddVelocity(Vector2 velocity) { _velocity += velocity; }

public:
	void SetMass(float mass) { _mass = mass; }
	void SetVelocity(Vector2 velocity) { _velocity = velocity; }
	void SetMaxVelocity(float maxVelocity) { _maxVelocity = maxVelocity; }

public:
	float	GetMass() { return _mass; }
	Vector2 GetVelocity() { return _velocity; }
	float	GetMaxVelocity() { return _maxVelocity; }

	friend class CObject;
};

